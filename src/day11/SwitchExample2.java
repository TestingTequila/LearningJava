package day11;

public class SwitchExample2
{
    public static void main(String[] args) {
        String day = "Friday";
        switch (day)
        {
            case "Monday":
                System.out.println("Its Monday, I will go to office");
                break;
            case "Tuesday":
                System.out.println("Its Tuesday, I will go to Factory");
                break;

        }
    }
}
