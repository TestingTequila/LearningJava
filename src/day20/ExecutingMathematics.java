package day20;

public class ExecutingMathematics
{
    public static void main(String[] args) {
        Mathematics maths = new Mathematics();
        System.out.println(maths.b1);//0
        System.out.println(maths.s1);//0
        System.out.println(maths.i1);//0
        System.out.println(maths.l1);//0
        System.out.println(maths.f1);//0.0
        System.out.println(maths.d1);//0.0
        System.out.println(maths.c1);//'nul'
        System.out.println(maths.b2);//false
        System.out.println(maths.s2);//null
    }
}
