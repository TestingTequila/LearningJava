package day20;

public class B
{
    public B()
    {
        System.out.println("default Constructor");
    }

    public B(int a)
    {
        System.out.println("constructor with one parameter: " + a);
    }

    public B(int a, int b)
    {
        System.out.println("constructor with two parameter: " + a + " and " + b);
    }
}
