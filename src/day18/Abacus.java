package day18;

public class Abacus
{
    public  Abacus()
    {
        System.out.println("I am the Constructor");
    }

    public  void Abacus()
    {
        System.out.println("I am the method");
    }

    public  void Abacus(int i)
    {
        System.out.println("I am the method" + i);
    }
}
